#include "../includes/ft_printf.h"

int	ft_checker(int c)
{
	if (c == 'c' || c == 's' || c == '%' || c == 'd'
		|| c == 'i' || c == 'p' || c == 'x' || c == 'X'
		|| c == 'u')
		return (1);
	else
		return (0);
}
